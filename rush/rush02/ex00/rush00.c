void	print_grid(int grid[4][4])
{
	int	a;
	int	b;

	a = 0;
	while(a < 4)
	{
		b = 0;
		while (b < 4)
		{
			ft_putchar(grid[a][b] + '0');
			if (b < 3)
				ft_putchar(' ');
			b++;
		}
		ft_putchar('\n');
		a++;
	}
}
